# SIMPLE CALCULATOR USING PYTHON:
input1 = int(input("Enter the first no: "))
input3 = input("Choose from operator '+','-','/','*','//','**',: ")
input2 = int(input("Enter the second no: "))
if input3 == "+":
    print(f"Answer: {input1 + input2}")
elif input3 == "-":
    print(f"Answer: {input1 - input2}")
elif input3 == "/":
    print(f"Answer: {input1 / input2}")
elif input3 == "*":
    print(f"Answer: {input1 * input2}")
elif input3 == "//":
    print(f"Answer: {input1 // input2}")
elif input3 == "**":
    print(f"Answer: {input1 ** input2}")
print("Thanks For Using...")













